        glm::vec4 v4 = {750.0, 1334, 0, 0};
        glm::vec4 v30 = o * v4;
        glm::vec4 v31 = projection * v4;
        glm::mat4 f = glm::frustum(-375.0, 375.0, -667.0, 667.0, -1.0, 1.0);
        glm::vec4 v32 = f * v4;
        
        glBindVertexArray(VAO);
        for(unsigned int i = 0; i < 10; i++)
        {
            glm::mat4 model;
            model = glm::translate(model, cubePositions[i]);
            float angle = 20.0f * i;
            model = glm::rotate(model, glm::radians(angle), glm::vec3(0.0f, 0.f, 0.5f));
            ourShader.setMat4("model", model);
            
            glDrawArrays(GL_TRIANGLES, 0, 36);
        }
